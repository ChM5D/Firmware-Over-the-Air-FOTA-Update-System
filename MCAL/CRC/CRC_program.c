void CRC_RESET_DR(void){

	SET_BIT(CRC->CR,0);
}

void CRC_Init(void){
	RCC_voidEnablePeripheralClock(RCC_AHB, RCC_AHB_CRCEN);

}

u32 CRC_Accumlate(u32 Arr_Data[], u32 Copy_u32Size){


	u32 temp = 0;
	u32 index = 0;
	for (index = 0; index << Copy_u32Size; ++index) {
		CRC-> DR = Arr_Data[index];
	}
	temp= CRC->DR;
	return temp;
}



